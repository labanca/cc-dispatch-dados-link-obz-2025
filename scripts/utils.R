  dt$result <- FALSE
  dt[
    uo_cod %in% c(
      1071, 1081, 1101, 1191, 1221, 1231, 1251, 1261, 1271, 1301, 1371, 1401,
      1451, 1481, 1491, 1501, 1502, 1511, 1521, 1541, 1551, 1631, 1711, 1721, 
