      2211, 2241, 2251, 2261, 2271, 2281, 2301, 2311, 2321, 2351, 2371, 2421, 
      2431, 2441, 2461, 3041, 3051, 3151, 4251, 4291, 4331, 4341, 4381
    ) &
      grupo_cod %in% c(3, 4, 5) &
      fonte_cod %in% c(
        10, 13, 15, 23, 26, 27, 29, 31, 32, 33, 46, 47, 48, 49,
        50, 52, 53, 54, 59, 60, 61, 71, 72, 82, 83, 91, 94
      ) &
      ipu_cod %in% c(1, 2),
